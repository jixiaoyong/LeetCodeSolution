object BestTimeToBuyAndSellStock {

    fun maxProfitTimeOut(prices: IntArray): Int {
        var result = 0;
        if (prices.isEmpty()) {
            return result
        }

        for (x in 0 until prices.size - 1) {
            val buyPrice = prices[x]
            for (j in (x + 1) until prices.size) {
                val sellPrice = prices[j];
                if (buyPrice < sellPrice) {
                    result = Math.max(result, sellPrice - buyPrice)
                }
            }
        }

        return result
    }

    /**
     * 核心思想：先从左往右找到一个比之前的数小的数min，然后再在该数的右边找到一个比他大的数max，计算他们之间的差值profit，
     * 然后再找比min大的数，得到新的差值profit，保留最大的profit
     * 如果在此过程中找到一个比之前的数小的数，就需要再在该数的右边找到一个比他大的数，计算他们之间的差值，与之前的profit比较，
     * 如此遍历完整个列表，得到最大的profit即为result
     *
     * 此方法与maxProfitTimeOut最大的区别在于将两次遍历的计算过程，变为了一次遍历
     * 计算收益的过程中，只考虑当前数和他右边的数（即买入股票的当天和之后的天数）的计算结果，不考虑之前的数和他左边的数的计算结果
     * 参考了双指针的思想
     * Runtime: 524 ms, faster than 90.65% of Kotlin online submissions for Best Time to Buy and Sell Stock.
     * Memory Usage: 56.8 MB, less than 82.31% of Kotlin online submissions for Best Time to Buy and Sell Stock.
     */
    fun maxProfit1(prices: IntArray): Int {
        var result = 0;
        if (prices.isEmpty()) {
            return result
        }
        // 默认最大、最小值都为第一个值，同理result也为0
        var minValueIndex = 0
        var maxValueIndex = 0

        // 只要遍历一次数组即可
        for (x in 1 until prices.size) {
            // 如果数组后面有比minValue小的，就记住他的下标
            if (prices[x] < prices[minValueIndex]) {
                minValueIndex = x
                // 一个数不可能即是最大值又是最小值，所以后续不用判断
                continue
            }
            // 如果一个数比minValue大，就记住他的下标
            if (prices[x] > prices[minValueIndex]) {
                maxValueIndex = x
                // 只有当maxValue在minValue右边的时候，才能计算收益
                if (maxValueIndex > minValueIndex) {
                    val profit = prices[maxValueIndex] - prices[minValueIndex]
                    // result保存最大的收益
                    result = Math.max(profit, result)
                }
            }
        }

        return result
    }
}

