/**
 * @author : jixiaoyong
 * @description ： 523. 连续的子数组和
 *
 * 给你一个整数数组 nums 和一个整数k ，编写一个函数来判断该数组是否含有同时满足下述条件的连续子数组：
 *
 * 子数组大小 至少为 2 ，且
 * 子数组元素总和为 k 的倍数。
 * 如果存在，返回 true ；否则，返回 false 。
 *
 * 如果存在一个整数 n ，令整数 x 符合 x = n * k ，则称 x 是 k 的一个倍数。0 始终视为 k 的一个倍数。
 *
 * 提示：
 *
 * 1 <= nums.length <= 10^5
 * 0 <= nums[i] <= 10^9
 * 0 <= sum(nums[i]) <= 2^31 - 1
 * 1 <= k <= 2^31 - 1
 *
 * @email : jixiaoyong1995@gmail.com
 * @date : 10/26/2022
 */
class ContinuousSubarraySum {

    /**
     * dp
     * 当前下标为x的数字和之前的所有数字的和f[x] = f[x-1]+nums[x] ,y>=1
     * 假设，j-i>=1，则f[i,j] = f[j] - f[i-1]
     * 如果f[i,j]满足条件，是k的倍数，那么f[j] - f[i-1] = nk
     * 也就有（f[j] - f[i-1]) / k = n
     * 因为n是整数，那么（f[j] - f[i-1]) / k 也必定是整数
     * 又有,一个数必定可以表示为 a*k + b （即为k的a倍+b，其中b<k），所以上面表达式可以写作：
     * （f[j] - f[i-1]) / k
     * = (（a1*k + b1）-（a2*k + b2）) /k
     * = a1 - a2 + (b1 - b2) /k
     * 上述等式要为整数，则b1必须等于b2，即f[j] 和 f[i-1] 的余数相等，同时，需要满足j - (i-1) >=2
     * 反之，如果存在两个数，他们的除k的余数b相等，就可以认为他们相减的数为k的倍数，也就是这个区间的连续子数组满足题目要求
     *
     * 时间复杂度 O（N）
     * 空间复杂度O(N) ，preSum占据N个空间
     */
    fun checkSubarraySum(nums: IntArray, k: Int): Boolean {
        val len = nums.size
        if (len == 1) {
            return false
        }
        val preSum = IntArray(nums.size)
        val hashMap = HashMap<Int, Int>()
        preSum[0] = nums[0]
        hashMap.put(preSum[0] % k, 0)

        for (x in 1 until len) {
            preSum[x] = preSum[x - 1] + nums[x]
            val remainder = preSum[x] % k
            if (remainder == 0) {
                // 为0，说明本身是K的倍数，直接返回
                return true
            } else if (hashMap.contains(remainder)) {
                // 规避[1,6] 6这种情况，f[i,j] = f[j] -f[i-1] ，f[j] 和 f[i-1] 的余数相等，
                // 同时，需要满足j - (i-1) >= 2,此处hashMap[remainder]即i-1，
                // 对于[1,6] 6，的第二个数字，num[1]来说，f[0,1] = f[1] - f[-1]
                // val pre = hashMap[remainder]
                // f[pre+1,x] = f[x] - f[pre]  , x- (pre+1) >= 1 ---> x- pre >=2  ---> x - pre > 1
                if (x - hashMap[remainder]!! > 1) {
                    return true
                }
            } else {
                // 此处x即为前0~x位数字之和与k的余数
                hashMap.put(remainder, x)
            }
        }

        return false
    }

    // 这里，分别以0~n，1~n，...，n-1~n计算可能的子数组，时间复杂度最坏为O(n^2)，会超时
    fun checkSubarraySumTimeout(nums: IntArray, k: Int): Boolean {
        val len = nums.size
        if (len == 1) {
            return false
        }
        for (x in 0 until len) {
            var pre = nums[x]
            for (y in x + 1 until len) {
                val cur = pre + nums[y]
                if (cur % k == 0) {
                    return true
                }
                pre = cur
            }

        }

        return false
    }

    /**
     * https://leetcode.cn/problems/continuous-subarray-sum/solution/lian-xu-de-zi-shu-zu-he-by-leetcode-solu-rdzi/
     *
     * 时间复杂度 O（N）
     * 空间复杂度O(min(N,k)) 主要空间在于hash表所占的空间，最大有min(N,k)个余数
     */
    fun checkSubarraySumLeetcode(nums: IntArray, k: Int): Boolean {
        val m = nums.size
        if (m < 2) {
            return false
        }
        val map: MutableMap<Int, Int> = HashMap()
        map[0] = -1
        var remainder = 0
        for (i in 0 until m) {
            remainder = (remainder + nums[i]) % k
            if (map.containsKey(remainder)) {
                val prevIndex = map[remainder]!!
                if (i - prevIndex >= 2) {
                    return true
                }
            } else {
                map[remainder] = i
            }
        }
        return false
    }
}

